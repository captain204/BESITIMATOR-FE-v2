const demoTheme = extendTheme({
  colorSchemes: { light: true, dark: true },
  colorSchemeSelector: "class",
  breakpoints: {
    values: {
      xs: 0,
      sm: 600,
      md: 600,
      lg: 1200,
      xl: 1536,
    },
  },
});

function DemoPageContent({ pathname }: { pathname: string }) {
  const getContentForPath = (path: string) => {
    switch (path) {
      case "/dashboard":
        return "Welcome to the Dashboard!";
      case "/orders":
        return "Here are your Orders.";
      case "/reports/sales":
        return <div> <BlogPosts/> </div>;
      case "/reports/traffic":
        return <PostBlog />;
      case "/integrations":
        return "Integrations Overview.";
      default:
        return "Select an item from the navigation.";
    }
  };

  return (
    <Box
      sx={{
        py: 4,
        display: "flex",
        flexDirection: "column",
        alignItems: "center",
        textAlign: "center",
      }}
    >
      <Typography variant="h4">{getContentForPath(pathname)}</Typography>
    </Box>
  );
}

function useDemoRouter(initialPath: string): Router {
  const [pathname, setPathname] = React.useState(initialPath);

  const router = React.useMemo(() => {
    return {
      pathname,
      searchParams: new URLSearchParams(),
      navigate: (path: string | URL) => setPathname(String(path)),
    };
  }, [pathname]);

  return router;
}

